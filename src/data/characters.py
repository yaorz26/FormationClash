CHARACTER_DEFINITIONS_BY_ID: dict[str, CharacterDefinition] = {
    character.id: character for character in CHARACTER_DEFINITIONS
}
TEST_CHARACTER_DEFINITIONS_BY_ID: dict[str, CharacterDefinition] = {
    character.id: character for character in TEST_CHARACTER_DEFINITIONS
}


def _create_pool(
    definitions: tuple[CharacterDefinition, ...],
    jobs_by_id: dict[str, Job] | None = None,
) -> list[Character]:
    jobs = jobs_by_id or JOBS_BY_ID
    return [definition.create_character(jobs) for definition in definitions]


def create_character_pool(jobs_by_id: dict[str, Job] | None = None) -> list[Character]:
    return _create_pool(CHARACTER_DEFINITIONS, jobs_by_id)


def create_draft_character_pool(
    jobs_by_id: dict[str, Job] | None = None,
    *,
    include_arcanarch: bool = False,
) -> list[Character]:
    excluded_job_ids = {"summon"}
    if not include_arcanarch:
        excluded_job_ids.add("arcanarch")
    return _create_pool(
        tuple(definition for definition in CHARACTER_DEFINITIONS if definition.job_id not in excluded_job_ids),
        jobs_by_id,
    )


def create_test_character_pool(jobs_by_id: dict[str, Job] | None = None) -> list[Character]:
    return _create_pool(TEST_CHARACTER_DEFINITIONS, jobs_by_id)
